#include "libft.h"

int	ft__doller(int c)
{
	if ((c >= '0' && c <= '9') || (c >= 'A' && c <= 'Z')
		|| c == '-' || c == '"' || c == '\'' || c == '$' || c > 'a' || c < 'z')
		return (1);
	return (0);
}
