#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	size_t	i;
	size_t	j;

	if (!s || !f)
		return (NULL);
	j = ft_strlen(s);
	str = malloc(j + 1);
	if (!str)
		malloc_exit();
	i = 0;
	while (s[i] != '\0')
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
