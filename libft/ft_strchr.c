#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		j;

	j = 0;
	while (s[j] != '\0' && s[j] != (char)c)
		j++;
	if (s[j] == (char)c)
		return ((char *)(s + j));
	return (NULL);
}
